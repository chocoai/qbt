package com.qbt.web.pojo.address;

import java.util.List;

public class ProvinceVo {
	
private Integer id;
	
	private String name;
	
	//1: active ; 2:inactive
	private Integer status;
	
	private Integer courseAmount;
	
	private Integer sortNumber;

	private List<CourseAddressVo> courseAddressVoList;
	
	


	public List<CourseAddressVo> getCourseAddressVoList() {
		return courseAddressVoList;
	}

	public void setCourseAddressVoList(List<CourseAddressVo> courseAddressVoList) {
		this.courseAddressVoList = courseAddressVoList;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getCourseAmount() {
		return courseAmount;
	}

	public void setCourseAmount(Integer courseAmount) {
		this.courseAmount = courseAmount;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}



	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Integer getSortNumber() {
		return sortNumber;
	}

	public void setSortNumber(Integer sortNumber) {
		this.sortNumber = sortNumber;
	}

}

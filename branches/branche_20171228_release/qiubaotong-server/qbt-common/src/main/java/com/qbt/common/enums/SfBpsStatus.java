package com.qbt.common.enums;


/**
 *  订单状态
  * @ClassName: OrderStatus
  * @Description: TODO
  * @author share 2016年8月13日
  * @modify share 2016年8月13日
 */
public enum SfBpsStatus {

	/**
	 * 顺丰BPS状态
		1-未发送
		2-发送成功
		3-发送失败
	 */
	un_send(1,"未发送"),
	sucess(2,"发送成功"),
	fail(3,"发送失败"),
	;
	
	Integer code;
	String desc;

	private SfBpsStatus(Integer code,String desc) { 
		this.code = code;
		this.desc = desc;
	}

	public Integer getCode() {
		return code;
	}

	public void setCode(Integer code) {
		this.code = code;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	
}

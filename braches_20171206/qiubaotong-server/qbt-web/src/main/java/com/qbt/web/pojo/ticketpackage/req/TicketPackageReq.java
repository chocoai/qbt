package com.qbt.web.pojo.ticketpackage.req;

public class TicketPackageReq {
	
	int packageId;
	String activateUser;
	String activatePhoneNumber;
	String recieveUserName;
	String recieveUserMobile;
	String qrCode;
	String url;
	int ticketId;
	public int getPackageId() {
		return packageId;
	}
	public void setPackageId(int packageId) {
		this.packageId = packageId;
	}
	public String getActivateUser() {
		return activateUser;
	}
	public void setActivateUser(String activateUser) {
		this.activateUser = activateUser;
	}
	public String getActivatePhoneNumber() {
		return activatePhoneNumber;
	}
	public void setActivatePhoneNumber(String activatePhoneNumber) {
		this.activatePhoneNumber = activatePhoneNumber;
	}
	public String getRecieveUserName() {
		return recieveUserName;
	}
	public void setRecieveUserName(String recieveUserName) {
		this.recieveUserName = recieveUserName;
	}
	public String getRecieveUserMobile() {
		return recieveUserMobile;
	}
	public void setRecieveUserMobile(String recieveUserMobile) {
		this.recieveUserMobile = recieveUserMobile;
	}
	public String getQrCode() {
		return qrCode;
	}
	public void setQrCode(String qrCode) {
		this.qrCode = qrCode;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getTicketId() {
		return ticketId;
	}
	public void setTicketId(int ticketId) {
		this.ticketId = ticketId;
	}
	

}
